    def log_event(self, event: Event):
        """Append event to tree log"""
        log_entry = event.to_log_entry()

        # Use atomic write pattern
        temp_path = f"{self.tree_path}.tmp"

        try:
            # Read existing content
            with open(self.tree_path, 'r') as f:
                content = f.read()

            # Find the log section
            if "## ğŸ“ Event Log" in content:
                # Append to log section
                parts = content.split("## ğŸ“ Event Log")
                if len(parts) == 2:
                    header = parts[0] + "## ğŸ“ Event Log"
                    log_section = parts[1]

                    # Add new entry
                    new_content = f"{header}{log_section}{log_entry}\n"
                else:
                    # Just append at end
                    new_content = content + f"\n{log_entry}\n"
            else:
                # No log section, append at end
                new_content = content + f"\n## ğŸ“ Event Log\n\n{log_entry}\n"

            # Write to temp file
            with open(temp_path, 'w') as f:
                f.write(new_content)

            # Atomic rename
            os.replace(temp_path, self.tree_path)

        except Exception as e:
            # Clean up temp file on error
            if os.path.exists(temp_path):
                os.remove(temp_path)
            print(f"âš ï¸  Error logging event: {e}")

    def add_reflection(self, reflection: str):
        """Add auto-reflection to tree"""
        timestamp = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
        reflection_entry = f"\n### Auto-Reflection [{timestamp}]\n{reflection}\n"

        temp_path = f"{self.tree_path}.tmp"

        try:
            with open(self.tree_path, 'r') as f:
                content = f.read()

            # Add reflection section if needed
            if "## ğŸ”® Auto-Reflections" not in content:
                content += "\n\n---\n\n## ğŸ”® Auto-Reflections\n"

            content += reflection_entry

            with open(temp_path, 'w') as f:
                f.write(content)

            os.replace(temp_path, self.tree_path)

        except Exception as e:
            if os.path.exists(temp_path):
                os.remove(temp_path)
            print(f"âš ï¸  Error adding reflection: {e}")
